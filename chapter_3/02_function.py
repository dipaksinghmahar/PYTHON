name="Dipak"
print(len(name)) #count length of the string
print(name.lower()) #convert to lower case
print(name.upper()) #convert to upper case
print(name.startswith("D")) #check if string starts with D
print(name.endswith("k")) #check if string ends with k
print(name.capitalize()) #capitalize the first letter of the string
print(name.find("a")) #find the index of the first occurrence or place of the a
print(name.replace("D","b")) #replace D with b